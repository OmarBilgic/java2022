package oop1;

public class CorparationCustomer extends Customer{
	private String corparationName;
	private String taxNumber;
	private String corparationNumber;
	public String getCorparationName() {
		return corparationName;
	}
	public void setCorparationName(String corparationName) {
		this.corparationName = corparationName;
	}
	public String getTaxNumber() {
		return taxNumber;
	}
	public void setTaxNumber(String taxNumber) {
		this.taxNumber = taxNumber;
	}
	public String getCorparationNumber() {
		return corparationNumber;
	}
	public void setCorparationNumber(String corparationNumber) {
		this.corparationNumber = corparationNumber;
	}
	
	
}
